    private void addCourseBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCourseBtnActionPerformed
        // TODO add your handling code here:
        String courseCode = courseCodeTextField.getText();
        String courseDesc = courseDescriptionTextField.getText();
        int seats = 0;
        if (courseCode.isEmpty()){
            courseStatusLabel.setText("You must add a course code");
        }else if(courseDesc.isEmpty()){
            courseStatusLabel.setText("You must add a course description.");
        }
        else{
            try
            {
                seatsSpinner.commitEdit();
                seats = (Integer) seatsSpinner.getValue();
            }
            catch(java.text.ParseException e)
            {
                System.out.println("Error: " + e + "\n Enter a value for seats.");
            }
            
            try
            {
                CourseEntry newCourse = new CourseEntry(currentSemester,courseCode,courseDesc,seats);
                CourseQueries.addCourse(newCourse);
                courseStatusLabel.setText("Course " + courseCodeTextField.getText() + " for semester " + currentSemester + " has been added.");
                rebuildScheduleCoursesPane();
            }
            catch(Exception e)
            {
                courseStatusLabel.setText("Course " + courseCodeTextField.getText() + " for semester " + currentSemester + " was not added. Possibly a duplicate.");
            }
        }
    }//GEN-LAST:event_addCourseBtnActionPerformed

