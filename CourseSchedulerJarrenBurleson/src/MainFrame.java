    public void rebuildSemesterComboBoxes() {
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        currentSemesterComboBox.setModel(new javax.swing.DefaultComboBoxModel(semesters.toArray()));
        if (semesters.size() > 0) {
            currentSemesterLabel.setText(semesters.get(0));
            currentSemester = semesters.get(0);
        } else {
            currentSemesterLabel.setText("None, add a semester.");
            currentSemester = "None";
        }
    }
    
    public void rebuildStudentComboBoxes(){
        studentIDs = new ArrayList<String>();
        studentNames = new ArrayList<String>();
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        for(int i= 0; i<students.size(); i++){
            studentNames.add(students.get(i).getLastName() + ", " + students.get(i).getFirstName());
            studentIDs.add(students.get(i).getStudentID());
        }
        studentComboBox.setModel(new javax.swing.DefaultComboBoxModel(studentNames.toArray()));
        studentDisplayScheduleComboBox.setModel(new javax.swing.DefaultComboBoxModel(studentNames.toArray()));
        if (studentComboBox.getSelectedItem() != null)
        {
            int position = studentNames.indexOf(studentComboBox.getSelectedItem().toString());
            currentStudent = studentIDs.get(position);
        }else
        {
            currentStudent = null;
        }
    }
    
    public void rebuildScheduleCoursesPane(){
        ArrayList<String> courses = CourseQueries.getAllCourseCodes(currentSemester);
        courseComboBox.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
        rebuildDisplayCourseTable();
    }
    
    public void rebuildDisplaySchedulePane(){
        rebuildDisplayScheduleTable();
    }
    
    public void rebuildDisplayCourseTable(){
        ArrayList<CourseEntry> courses = CourseQueries.getAllCourses(currentSemester);
        
        DefaultTableModel model = (DefaultTableModel) displayCourseTable.getModel();
        model.setRowCount(0);
        
        if (courses!= null){
            for (CourseEntry currentCourse: courses){
                Object[] row = {currentCourse.getCourseCode(), currentCourse.getCourseDesc(), currentCourse.getSeats()};
                model.addRow(row);
            }
        }
    }
       
    public void rebuildDisplayScheduleTable(){
        ArrayList<ScheduleEntry> schedule = ScheduleQueries.getScheduleByStudent(currentSemester, currentStudent);
        
        DefaultTableModel model = (DefaultTableModel) displayScheduleTable.getModel();
        model.setRowCount(0);
        if (schedule!= null){
            for (ScheduleEntry currentSchedule: schedule){
                Object[] row = {currentSchedule.getCourseCode(), currentSchedule.getStatus()};
                model.addRow(row);
            }
        }    
    }
            
