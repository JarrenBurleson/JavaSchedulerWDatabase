    private void addStudentBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addStudentBtnActionPerformed
        // TODO add your handling code here:
        String studentID = studentIDTextField.getText();
        String firstName = firstNameTextField.getText();
        String lastName = lastNameTextField.getText();
        if (studentID.isEmpty()){
            studentAddedStatusLabel.setText("You must add a student ID");
        }else if (firstName.isEmpty()){
            studentAddedStatusLabel.setText("You must add a first name");
        }else if (lastName.isEmpty()){
            studentAddedStatusLabel.setText("You must add a last name");
        }else{
            try{
                StudentEntry newStudent = new StudentEntry(studentID,firstName,lastName);
                StudentQueries.addStudent(newStudent);
                studentAddedStatusLabel.setText("Student " + firstName + " " + lastName + " has been added.");
                rebuildScheduleCoursesPane();
                rebuildStudentComboBoxes();
            }catch (Exception e){
                studentAddedStatusLabel.setText("A student with that Student ID already exists.");
            }
        }
        
    }//GEN-LAST:event_addStudentBtnActionPerformed

    private void submitScheduleChangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitScheduleChangeActionPerformed
        // TODO add your handling code here:
        String courseCode = courseComboBox.getSelectedItem().toString();
        String studentID = studentIDs.get(studentComboBox.getSelectedIndex());
        String studentName = studentComboBox.getSelectedItem().toString();
        String status;
        int seats = CourseQueries.getCourseSeats(currentSemester, courseCode);

        if (seats>0){
            status = "s";
            CourseQueries.updateSeats(currentSemester, courseCode, (seats-1));
            addScheduleStatusLabel.setText(studentName + " was succesfully added to " + courseCode);
        }else{
            status = "w";
            addScheduleStatusLabel.setText(studentName + " was waitlisted for " + courseCode);
        }
        
        try{
            ScheduleEntry newSchedule = new ScheduleEntry(currentSemester, courseCode, studentID, status);
            ScheduleQueries.addScheduleEntry(newSchedule);
        }catch(Exception e){
            addScheduleStatusLabel.setText(studentName + " is already scheduled/waitlisted for " + courseCode);
        }
    }//GEN-LAST:event_submitScheduleChangeActionPerformed

